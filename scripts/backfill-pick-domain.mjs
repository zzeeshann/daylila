#!/usr/bin/env node
/**
 * One-shot backfill for daily_pieces.pick_domain on the last 30 published
 * pieces. PR #1 (2026-05-09) added the column; pre-deploy rows are NULL.
 * Without backfill, Curator's recent-domain soft-preference signal sees
 * an empty trailing window for ~30 days post-deploy.
 *
 * Two-stage like scripts/categoriser-cleanup-plan.mjs:
 *
 *   Stage A: read the last 30 pieces from prod D1, ask Claude to classify
 *            each into one of the 10 PickDomain enum values from the
 *            curator contract. Output: UPDATE SQL to scripts/
 *            backfill-pick-domain.sql for operator review. ZERO D1 writes.
 *
 *   Stage B (operator step, not in this script): review the SQL, then
 *            wrangler d1 execute zeemish --remote --file=scripts/backfill-pick-domain.sql
 *
 * Usage (from a canonical checkout, NOT a worktree without node_modules):
 *
 *   ANTHROPIC_API_KEY=sk-ant-... node scripts/backfill-pick-domain.mjs
 *
 * Requires wrangler on $PATH (read-only D1 access only) and the standard
 * Cloudflare auth. Re-runnable; regenerating the SQL gives Claude a fresh
 * pass against the contract.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { spawnSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import Anthropic from '@anthropic-ai/sdk';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, '..');

const ANTHROPIC_API_KEY = process.env.ANTHROPIC_API_KEY;
if (!ANTHROPIC_API_KEY) {
  console.error('ANTHROPIC_API_KEY not set.');
  process.exit(1);
}

const PICK_DOMAINS = [
  'inner-life',
  'meaning',
  'expression',
  'language',
  'science',
  'body',
  'how-humans-live',
  'skills',
  'technology',
  'time-and-place',
];

const CURATOR_CONTRACT = readFileSync(
  join(REPO_ROOT, 'content/curator-contract.md'),
  'utf8',
);

console.log('Fetching last 30 published pieces from prod D1...');
const wranglerOut = spawnSync(
  'wrangler',
  [
    'd1',
    'execute',
    'zeemish',
    '--remote',
    '--json',
    '--command',
    `SELECT id, date, headline, underlying_subject FROM daily_pieces
     WHERE pick_domain IS NULL
     ORDER BY published_at DESC
     LIMIT 30`,
  ],
  { encoding: 'utf8', cwd: REPO_ROOT },
);

if (wranglerOut.status !== 0) {
  console.error('wrangler failed:', wranglerOut.stderr);
  process.exit(1);
}

const wranglerJson = JSON.parse(wranglerOut.stdout);
const rows = wranglerJson?.[0]?.results ?? [];
console.log(`Found ${rows.length} pieces to classify.`);

if (rows.length === 0) {
  console.log('Nothing to backfill. Exiting.');
  process.exit(0);
}

const client = new Anthropic({ apiKey: ANTHROPIC_API_KEY });

const prompt = `You are classifying past Daylila pieces into the 10-domain teachability taxonomy from the Curator contract below. For each piece, return the single domain whose lens does the most teaching work — the SUBJECT-of-the-teaching domain, not the surface category of the news (a Taylor Swift tour-economics piece is "expression" or "how-humans-live", not surface-business just because it's about money).

Closed enum (use these exact slugs): ${PICK_DOMAINS.join(', ')}

${CURATOR_CONTRACT}

## Pieces to classify

${rows.map((r, i) => `${i + 1}. id=${r.id}\n   headline: "${r.headline}"\n   underlying subject: ${r.underlying_subject ?? '(none)'}`).join('\n\n')}

Return JSON only:
{
  "classifications": [
    { "id": "<piece uuid>", "domain": "<one of the 10 enum slugs>" }
  ]
}

One entry per piece in the input order. No commentary.`;

console.log('Calling Claude...');
const resp = await client.messages.create({
  model: 'claude-sonnet-4-5-20250929',
  max_tokens: 4000,
  messages: [{ role: 'user', content: prompt }],
});

const text = resp.content[0].type === 'text' ? resp.content[0].text : '{}';
const jsonMatch = text.match(/\{[\s\S]*\}/);
if (!jsonMatch) {
  console.error('Claude returned no JSON:');
  console.error(text);
  process.exit(1);
}

const parsed = JSON.parse(jsonMatch[0]);
const classifications = parsed.classifications ?? [];

if (classifications.length !== rows.length) {
  console.warn(
    `Warning: Claude returned ${classifications.length} classifications for ${rows.length} pieces. Inspect output before applying.`,
  );
}

const sqlLines = [
  '-- Backfill daily_pieces.pick_domain for the last 30 published pieces.',
  '-- Generated by scripts/backfill-pick-domain.mjs (PR #1, 2026-05-09).',
  '-- Review domain assignments before applying via:',
  '--   wrangler d1 execute zeemish --remote --file=scripts/backfill-pick-domain.sql',
  '',
];

for (const c of classifications) {
  if (!PICK_DOMAINS.includes(c.domain)) {
    sqlLines.push(
      `-- SKIPPED: ${c.id} — Claude returned unknown domain "${c.domain}". Hand-classify if needed.`,
    );
    continue;
  }
  const piece = rows.find((r) => r.id === c.id);
  const headline = piece?.headline?.replace(/'/g, "''") ?? '';
  sqlLines.push(`-- ${headline}`);
  sqlLines.push(
    `UPDATE daily_pieces SET pick_domain = '${c.domain}' WHERE id = '${c.id}';`,
  );
  sqlLines.push('');
}

const outPath = join(REPO_ROOT, 'scripts/backfill-pick-domain.sql');
writeFileSync(outPath, sqlLines.join('\n'));
console.log(`Wrote ${classifications.length} UPDATEs to ${outPath}`);
console.log('Review then apply:');
console.log('  wrangler d1 execute zeemish --remote --file=scripts/backfill-pick-domain.sql');
