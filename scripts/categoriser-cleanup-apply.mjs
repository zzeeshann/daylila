#!/usr/bin/env node
/**
 * Layer 3 Stage B — read the operator-edited cleanup plan from
 * `scripts/categoriser-cleanup-plan.json` and generate a forward-only
 * D1 migration at `migrations/0039_categoriser_cleanup.sql`.
 *
 * **Does NOT execute the migration.** The operator runs
 * `wrangler d1 migrations apply zeemish --remote` themselves after
 * reviewing the generated SQL.
 *
 * Usage:
 *
 *   node scripts/categoriser-cleanup-apply.mjs
 *
 * Reads:  scripts/categoriser-cleanup-plan.json (operator-edited)
 * Writes: migrations/0039_categoriser_cleanup.sql (forward-only)
 *
 * Migration shape:
 *   1. INSERT new target categories (fresh UUIDs, locked=1).
 *   2. DELETE all piece_categories rows for any piece named in
 *      piece_reassignments. (Cleaner than per-row UPDATE when the
 *      target set differs in count from the source set.)
 *   3. INSERT fresh piece_categories rows per the reassignment plan,
 *      confidence=100 (operator-curated).
 *   4. DELETE old categories that now have zero piece_categories rows.
 *   5. Recompute categories.piece_count from piece_categories.
 *
 * Note on the locked flag: the migration sets locked=1 on every new
 * operator-curated target category. CategoriserAgent only INSERTs (it
 * never DELETEs or re-tags), so the flag is currently inert at the
 * agent layer — its purpose is to document operator-curation intent
 * for future drift-handling (e.g. a CategoriserAuditor agent or a
 * future merge/delete admin path).
 *
 * If the plan JSON is missing required fields, this script exits
 * non-zero with a diagnostic. The plan from Stage A is well-formed by
 * default; failures here usually mean the operator removed a required
 * field during inline editing.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { randomUUID } from 'node:crypto';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, '..');
const PLAN_PATH = join(__dirname, 'categoriser-cleanup-plan.json');
const MIGRATION_PATH = join(REPO_ROOT, 'migrations', '0039_categoriser_cleanup.sql');

let plan;
try {
  plan = JSON.parse(readFileSync(PLAN_PATH, 'utf8'));
} catch (err) {
  console.error(`Could not read ${PLAN_PATH}:`, err.message);
  console.error('Run scripts/categoriser-cleanup-plan.mjs first to generate the plan.');
  process.exit(1);
}

const targetCategories = plan.target_categories;
const reassignments = plan.piece_reassignments;
const disposition = plan.disposition;

if (!Array.isArray(targetCategories) || targetCategories.length === 0) {
  console.error('plan.target_categories must be a non-empty array.');
  process.exit(2);
}
if (!Array.isArray(reassignments) || reassignments.length === 0) {
  console.error('plan.piece_reassignments must be a non-empty array.');
  process.exit(3);
}
if (!Array.isArray(disposition)) {
  console.error('plan.disposition must be an array.');
  process.exit(4);
}

/** SQLite-safe single-quote escape. */
function sqlString(s) {
  if (s === null || s === undefined) return 'NULL';
  return `'${String(s).replace(/'/g, "''")}'`;
}

const now = Date.now();

// Generate fresh UUIDs upfront so the migration SQL has hard-coded
// values (no SQLite UUID generation needed).
const targetIds = new Map();
for (const t of targetCategories) {
  if (!t.slug || !t.name || !t.description) {
    console.error('target_categories entry missing slug/name/description:', t);
    process.exit(5);
  }
  targetIds.set(t.slug, randomUUID());
}

// Collect every piece_id that's being reassigned — these are the rows
// we wipe in the DELETE step before re-inserting.
const reassignedPieceIds = new Set(reassignments.map((r) => r.piece_id));

// Collect every old slug that's being merged or retired — these are
// the candidates for deletion at the end.
const oldSlugsToCheck = new Set();
for (const d of disposition) {
  if (d.action === 'merge_into' || d.action === 'retire' || d.action === 'rename_to') {
    if (d.old_slug) oldSlugsToCheck.add(d.old_slug);
  }
}

// ── Build the migration ─────────────────────────────────────────────

const lines = [];
lines.push('-- Migration 0039: Categoriser taxonomy cleanup (Layer 3)');
lines.push('--');
lines.push('-- Generated by scripts/categoriser-cleanup-apply.mjs from');
lines.push('-- scripts/categoriser-cleanup-plan.json (operator-edited).');
lines.push('--');
lines.push('-- See docs/DECISIONS.md 2026-05-07 "Categoriser fragmentation fix"');
lines.push('-- for the architectural context. Layer 1+2 (contract rewrite +');
lines.push('-- recent-headlines signal) shipped in the prior commit; this');
lines.push('-- migration handles the existing 26-category mess by collapsing');
lines.push(`-- into ${targetCategories.length} domain-level single-word categories.`);
lines.push('--');
lines.push('-- Forward-only. The fallback category (patterns-yet-to-cluster)');
lines.push('-- and any other category the plan does not explicitly retire are');
lines.push('-- preserved. This migration is operator-curated — confidence on');
lines.push('-- new piece_categories rows is set to 100.');
lines.push('');

lines.push('-- 1. INSERT new operator-curated target categories.');
for (const t of targetCategories) {
  const id = targetIds.get(t.slug);
  lines.push(
    `INSERT INTO categories (id, slug, name, description, locked, piece_count, created_at, updated_at)`,
  );
  lines.push(
    `VALUES (${sqlString(id)}, ${sqlString(t.slug)}, ${sqlString(t.name)}, ${sqlString(t.description)}, 1, 0, ${now}, ${now});`,
  );
}
lines.push('');

lines.push('-- 2. Delete all existing piece_categories rows for pieces being');
lines.push('--    reassigned. Cleaner than per-row UPDATEs when target counts');
lines.push('--    differ from source counts; we rebuild from scratch in step 3.');
const reassignedIdList = [...reassignedPieceIds].map(sqlString).join(',\n  ');
lines.push(`DELETE FROM piece_categories WHERE piece_id IN (\n  ${reassignedIdList}\n);`);
lines.push('');

lines.push('-- 3. INSERT fresh piece_categories rows per the cleanup plan.');
lines.push('--    Confidence=100 means operator-curated.');
for (const r of reassignments) {
  if (!r.piece_id || !Array.isArray(r.to) || r.to.length === 0) {
    console.error('reassignment missing piece_id or to[]:', r);
    process.exit(6);
  }
  for (const targetSlug of r.to) {
    const targetId = targetIds.get(targetSlug);
    if (!targetId) {
      console.error(
        `reassignment references target slug "${targetSlug}" not in target_categories:`,
        r,
      );
      process.exit(7);
    }
    lines.push(
      `INSERT INTO piece_categories (piece_id, category_id, confidence, created_at) VALUES (${sqlString(r.piece_id)}, ${sqlString(targetId)}, 100, ${now});`,
    );
  }
}
lines.push('');

lines.push('-- 4. Delete old categories that now have zero piece_categories rows.');
lines.push('--    Filtering on the JOIN preserves piece_categories integrity (we');
lines.push('--    do NOT touch the audit trail). Old categories that some piece');
lines.push('--    still references are left untouched — operator can rerun the');
lines.push('--    cleanup if more pieces need migrating.');
if (oldSlugsToCheck.size > 0) {
  const oldSlugList = [...oldSlugsToCheck].map(sqlString).join(',\n  ');
  lines.push(`DELETE FROM categories WHERE slug IN (\n  ${oldSlugList}\n) AND id NOT IN (SELECT DISTINCT category_id FROM piece_categories);`);
} else {
  lines.push('-- (No old slugs marked for retirement in disposition.)');
}
lines.push('');

lines.push('-- 5. Recompute piece_count for every remaining category from the');
lines.push('--    actual piece_categories rows. Drift-recovery hatch baked into');
lines.push('--    the migration so we land in a known-good state.');
lines.push('UPDATE categories SET');
lines.push('  piece_count = (SELECT COUNT(*) FROM piece_categories WHERE category_id = categories.id),');
lines.push(`  updated_at = ${now};`);
lines.push('');

writeFileSync(MIGRATION_PATH, lines.join('\n') + '\n', 'utf8');

console.error(`Wrote ${MIGRATION_PATH}`);
console.error(`  ${targetCategories.length} new categories.`);
console.error(`  ${reassignedPieceIds.size} pieces reassigned.`);
console.error(`  ${oldSlugsToCheck.size} old categories candidates for deletion.`);
console.error('');
console.error('Next steps:');
console.error(`  1. Read ${MIGRATION_PATH} end-to-end. Sanity-check the SQL.`);
console.error('  2. Apply against prod:');
console.error('       wrangler d1 migrations apply zeemish --remote');
console.error('  3. Verify:');
console.error('       wrangler d1 execute zeemish --remote --command \\');
console.error('         "SELECT slug, name, piece_count FROM categories ORDER BY piece_count DESC"');
console.error('  4. Visit /library/ and confirm the chip bar reads cleanly.');
